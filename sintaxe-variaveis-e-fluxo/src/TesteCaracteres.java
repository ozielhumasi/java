
public class TesteCaracteres {

	public static void main(String[] args) {
		char letra = 'a';
		System.out.println(letra);
		
		char valor = 66;
		System.out.println(valor);
		
		valor = (char) (valor + 1);
		System.out.println(valor);
		
		String frase = "Minha primeira String em Java";
		System.out.println(frase);
		
		frase += " " + 2020;
		System.out.println(frase);
	}

}
